                <h3>Web Service</h3>
                <p><br>Web service adalah aplikasi sekumpulan data (database), perangkat lunak (software) atau bagian dari</br>
                perangkat lunak yangdapat diakses secara remote oleh berbagai piranti dengan sebuah perantara tertentu. 
                 Secara umum,web service  dapat diidentifikasikan dengan menggunakan URL seperti hanya web pada umumnya. 
                 Namun yang membedakan web service dengan web pada umumnya adalah interaksi yang diberikan oleh web service. 
                 Berbeda dengan URL web pada umumnya, URL web service hanya menggandung kumpulan informasi, perintah, 
                 konfigurasi atau sintaks yang berguna membangun sebuah fungsi-fungsi tertentu dari aplikasi.</p>
                <P>Web service dapat diartikan juga sebuah metode pertukaran data, tanpa memperhatikan dimana sebuahdatabase ditanamkan, 
                dibuat dalam bahasa apa sebuah aplikasi yang mengkonsumsi data, dan di platform apa sebuah data itu dikonsumsi. 
                Web service mampu menunjang interoperabilitas. Sehingga web service mampu menjadi sebuah jembatan penghubung antara 
                berbagai sistem yang ada.</P>
                <p>Menurut W3C Web services Architecture Working Group pengertian Web service adalah sebuah sistem softwareyang 
                di desain untuk mendukung interoperabilitas interaksi mesin ke mesin melalui sebuah jaringan. Interfaceweb service dideskripsikan dengan menggunakan format yang mampu diproses oleh mesin (khususnya WSDL). Sistem lain yang 
                akan berinteraksi dengan web service hanya memerlukan SOAP, yang biasanya disampaikan dengan HTTP dan XML 
                sehingga mempunyai korelasi dengan standar Web (Web Services Architecture Working Group, 2004).</p>
                <p>Sedangkan pada web service hubungan antara client dan server tidak terjadi secara langsung.
                 Hubungan antaraclient dan server dijembatani oleh file web service dalam format tertentu. 
                 Sehingga akses terhadap databaseakan ditanggani tidak secara langsung oleh server, 
                 melainkan melalui perantara yang disebut sebagai web service. 
                 Peran dari web service ini akan mempermudah distribusi sekaligus 
                 integrasi database yang tersebar di beberapa server sekaligus.</p>
